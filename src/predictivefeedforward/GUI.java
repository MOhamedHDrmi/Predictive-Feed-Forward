    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        JFileChooser fl = new JFileChooser();
        fl.setCurrentDirectory(new File("C:\\Users\\HDrmi\\Documents\\NetBeansProjects\\PredictiveFeedForward"));
        fl.setDialogTitle("Choose Image");
        if (fl.showOpenDialog(this) == JFileChooser.APPROVE_OPTION) {
            path = fl.getSelectedFile().getAbsolutePath();
        } else {
            JOptionPane.showMessageDialog(null, "No File Choosen", "", JOptionPane.CANCEL_OPTION);
        }
        jLabel1.setText(path);
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int bit = Integer.parseInt(jTextField1.getText());
        predictiveFeedForward.compress(path, (int)Math.pow(2, bit));
        jLabel2.setText("Successfully Compress");
    }//GEN-LAST:event_jButton2ActionPerformed

